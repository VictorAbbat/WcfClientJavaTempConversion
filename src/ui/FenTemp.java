    private static Double convertirVersCelcius(java.lang.Double temp, java.lang.String userName, java.lang.String machineName) {
        org.tempuri.ServiceConversion service = new org.tempuri.ServiceConversion();
        org.tempuri.IServiceConversion port = service.getBasicHttpBindingIServiceConversion();
        return port.convertirVersCelcius(temp, userName, machineName);
    }

    private static Double convertirVersFarh(java.lang.Double temp, java.lang.String userName, java.lang.String machineName) {
        org.tempuri.ServiceConversion service = new org.tempuri.ServiceConversion();
        org.tempuri.IServiceConversion port = service.getBasicHttpBindingIServiceConversion();
        return port.convertirVersFarh(temp, userName, machineName);
    }

    private static ArrayOfConversion getAll() {
        org.tempuri.ServiceConversion service = new org.tempuri.ServiceConversion();
        org.tempuri.IServiceConversion port = service.getBasicHttpBindingIServiceConversion();
        return port.getAll();
    }
    
    ///A reprendre
    private void afficherTableAppel(ArrayOfConversion listAppel, JTable table) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (Conversion conversion : listAppel.getConversion()) {
            model.addRow(new Object[]{conversion.getUserName().getValue(), conversion.getMachineName().getValue(), conversion.getValeur(),conversion.getDateAppel().toGregorianCalendar().getTime()});
        }
    }
